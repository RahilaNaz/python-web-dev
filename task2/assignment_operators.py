# Initial value
x = 10

# Assignment operators
print("Initial value of x:", x)

x += 5  # Same as: x = x + 5
print("After x += 5:", x)

x -= 3  # Same as: x = x - 3
print("After x -= 3:", x)

x *= 2  # Same as: x = x * 2
print("After x *= 2:", x)

x /= 4  # Same as: x = x / 4
print("After x /= 4:", x)

x %= 3  # Same as: x = x % 3
print("After x %= 3:", x)

x **= 2  # Same as: x = x ** 2
print("After x **= 2:", x)

x //= 2  # Same as: x = x // 2
print("After x //= 2:", x) 